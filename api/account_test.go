func TestListAccounts(t *testing.T) {
	type Query struct {
		Page int
		Size int
	}

	const n = 5

	accounts := make([]db.Account, n)

	for i := 0; i < 5; i++ {
		accounts[i] = randomAccount()
	}

	testCases := []struct {
		name          string
		query         Query
		buildStubs    func(store *mockdb.MockStore)
		checkResponse func(t *testing.T, recorder *httptest.ResponseRecorder)
	}{
		{
			name: "OK",
			query: Query{
				Page: 1,
				Size: n,
			},
			buildStubs: func(store *mockdb.MockStore) {
				arg := db.ListAccountsParams{
					Offset: 0,
					Limit:  5,
				}
				store.EXPECT().ListAccounts(gomock.Any(), gomock.Eq(arg)).Times(1).Return(accounts, nil)
			},
			checkResponse: func(t *testing.T, recorder *httptest.ResponseRecorder) {
				require.Equal(t, recorder.Code, http.StatusOK)
				requireMatchAccounts(t, recorder.Body, accounts)
			},
		},
		{
			name: "BadRequest",
			query: Query{
				Page: -1,
				Size: 11,
			},
			buildStubs: func(store *mockdb.MockStore) {
				store.EXPECT().ListAccounts(gomock.Any(), gomock.Any()).Times(0)
			},
			checkResponse: func(t *testing.T, recorder *httptest.ResponseRecorder) {
				require.Equal(t, recorder.Code, http.StatusBadRequest)
			},
		},
		{
			name: "Internal Server Error",
			query: Query{
				Page: 1,
				Size: n,
			},
			buildStubs: func(store *mockdb.MockStore) {
				arg := db.ListAccountsParams{
					Offset: 0,
					Limit:  n,
				}

				store.EXPECT().ListAccounts(gomock.Any(), gomock.Eq(arg)).Times(1).Return([]db.Account{}, sql.ErrConnDone)
			},
			checkResponse: func(t *testing.T, recorder *httptest.ResponseRecorder) {
				require.Equal(t, recorder.Code, http.StatusInternalServerError)
			},
		},
	}

	for i := range testCases {
		tc := testCases[i]

		t.Run(tc.name, func(t *testing.T) {
			ctrl := gomock.NewController(t)
			defer ctrl.Finish()

			store := mockdb.NewMockStore(ctrl)

			tc.buildStubs(store)

			server := newTestServer(t, store)

			recorder := httptest.NewRecorder()

			url := "/accounts"

			request, err := http.NewRequest(http.MethodGet, url, nil)
			require.NoError(t, err)

			// Add query parameters to request URL
			q := request.URL.Query()
			q.Add("page", fmt.Sprintf("%d", tc.query.Page))
			q.Add("size", fmt.Sprintf("%d", tc.query.Size))

			request.URL.RawQuery = q.Encode()

			server.router.ServeHTTP(recorder, request)
			tc.checkResponse(t, recorder)
		})
	}
}

func requireBodyMatchAccount(t *testing.T, body *bytes.Buffer, account db.Account) {
	data, err := ioutil.ReadAll(body)
	require.NoError(t, err)

	var foundAccount db.Account

	err = json.Unmarshal(data, &foundAccount)
	require.NoError(t, err)
	require.Equal(t, account, foundAccount)
}

func requireMatchAccounts(t *testing.T, body *bytes.Buffer, accounts []db.Account) {
	data, err := ioutil.ReadAll(body)
	require.NoError(t, err)

	var foundAccounts []db.Account

	err = json.Unmarshal(data, &foundAccounts)
	require.NoError(t, err)
	require.Equal(t, foundAccounts, accounts)
}

func randomAccount() db.Account {
	return db.Account{
		Owner:    util.RandomOwner(),
		Balance:  util.RandomMoney(),
		ID:       util.RandomInt(1, 100),
		Currency: util.RandomCurrency(),
	}
}
